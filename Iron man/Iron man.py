def circle():
    glClearColor(1,1,1,1)
    glClear(GL_COLOR_BUFFER_BIT)
    glColor(1,0,0)

    glBegin(GL_POLYGON)
    for theta in np.arange(0,2*pi,0.001):
        x=0.5 * cos(theta)
        y=0.5 * sin(theta)
        glVertex2d(x,y)
    glEnd()
    glFlush()

